/**
 * 주어진 단어 풀에서 개수만큼 중복 없이 랜덤한 단어를 추출합니다.
 * @param count 추출할 단어 개수
 * @param exclude 현재 화면에 있는 단어 등 중복 배제할 리스트
 */
export function getRandomWords(count: number, exclude: string[] = []): string[] {
  const availableWords = WORD_POOL.filter(w => !exclude.includes(w));
  const result: string[] = [];
  const tempPool = [...availableWords];

  for (let i = 0; i < count; i++) {
    if (tempPool.length === 0) break;
    const randomIndex = Math.floor(Math.random() * tempPool.length);
    result.push(tempPool[randomIndex]);
    tempPool.splice(randomIndex, 1);
  }

  return result;
}
